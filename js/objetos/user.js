
class user{
    constructor(generateId, name, username, mail, password){
        this.id = generateId
        this.nombre = name
        this.username = username
        this.email = mail
        this.password = password
    }

    getID() {
        return this.id
    }

    getNombre() {
        return this.fecha
    }

    getUsername() {
        return this.cliente
    }

    getEmail() {
        return this.vendedor
    }

    getPassword() {
        return this.isCredito
    }

    setNombre(nombre) {
        this.nombre = this.nombre
    }

    setUsername(username) {
        this.username = this.username
    }

    setEmail(email) {
        this.email = email
    }

    setPassword(password) {
        this.password = password
    }
}